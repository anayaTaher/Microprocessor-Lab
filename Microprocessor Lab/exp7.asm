ASSUME CS:CODE
ORG 1000H

START:

BRUH:
MOV DX, 0FF2BH
MOV AL, 80H
OUT DX, AL

;ROW
MOV DX, 0FF2AH
MOV AL, 00100011B
OUT DX, AL

;COL
MOV DX, 0FF28H
MOV AL, 11111110B
OUT DX, AL

CALL COUNT

;ROW
MOV DX, 0FF2AH
MOV AL, 01000101B
OUT DX, AL

;COL
MOV DX, 0FF28H
MOV AL, 11111101B
OUT DX, AL

CALL COUNT

;ROW
MOV DX, 0FF2AH
MOV AL, 01001001B
OUT DX, AL

;COL
MOV DX, 0FF28H
MOV AL, 11111011B
OUT DX, AL

CALL COUNT

;ROW
MOV DX, 0FF2AH
MOV AL, 01001001B
OUT DX, AL

;COL
MOV DX, 0FF28H
MOV AL, 11110111B
OUT DX, AL

CALL COUNT

;ROW
MOV DX, 0FF2AH
MOV AL, 00110001B
OUT DX, AL

;COL
MOV DX, 0FF28H
MOV AL, 11101111B
OUT DX, AL

CALL COUNT

JMP BRUH


COUNT PROC NEAR
      MOV CX, 1000
      L1:
      NOP
      LOOP L1
      RET
COUNT ENDP

END START
CODE ENDS