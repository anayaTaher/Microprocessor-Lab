CODE SEGMENT
ASSUME CS:CODE
ORG 1000H
START:

; KEYPAD CONFIGURATION
MOV DX, 8001H
MOV AL, 0
OUT DX, AL

; PROGRAM CLOCK CONFIGURATION
MOV AL, 00110010B
OUT DX, AL

; CLEAR CONFIGURATION
MOV AL, 11011111B
OUT DX, AL

CALL DELAY

MOV DX, 8001H
MOV AL, 82H
OUT DX, AL

MOV DX, 8000H
L2:
MOV CX, 10
LEA SI, ARR
L3:
MOV AL, [SI]
OUT DX, AL
INC SI
PUSH CX
CALL DELAY
POP CX
LOOP L3
JMP L2


DELAY PROC NEAR
      MOV CX, 50000
      L1:
      NOP
      LOOP L1
      RET
DELAY ENDP

ARR DB 0CH, 9FH, 4AH, 0BH, 99H, 29H, 28H, 8FH, 8H, 9H

END START
CODE ENDS
