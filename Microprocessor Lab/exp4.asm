START:

; KEYPAD CONFIGURATION
MOV DX, 8001H
MOV AL, 0
OUT DX, AL

; PROGRAM CLOCK CONFIGURATION
MOV AL, 00110010B
OUT DX, AL

; CLEAR CONFIGURATION
MOV AL, 11011111B
OUT DX, AL

CALL DELAY

MOV DX, 8001H
MOV AL, 84H
OUT DX, AL

MOV DX, 8000H
MOV AL, 00101000B
OUT DX, AL

CALL DELAY

MOV DX, 8001H
MOV AL, 83H
OUT DX, AL

MOV DX, 8000H
MOV AL, 11000000B
OUT DX, AL

CALL DELAY

MOV DX, 8001H
MOV AL, 82H
OUT DX, AL

MOV DX, 8000H
MOV AL, 10011111B
OUT DX, AL

CALL DELAY

DELAY PROC NEAR USES CX
      MOV CX, 1000
      L1:
      NOP
      LOOP L1
      RET
DELAY ENDP

END START
